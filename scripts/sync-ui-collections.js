#!/usr/bin/env node
/**
 * Shriyam — UI Collection Sync Agent
 *
 * Reads live Shopify collections and product counts, then rewrites
 * app/lib/collectionConfig.js so the storefront always reflects what's in
 * the store. Run after sync-drive-to-shopify.js, or on a schedule.
 *
 * Run:  node scripts/sync-ui-collections.js [--dry-run]
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = join(SCRIPT_DIR, '..');
const CONFIG_FILE = join(PROJECT_ROOT, 'app/lib/collectionConfig.js');
const DRY_RUN = process.argv.includes('--dry-run');

// ── .env loader ───────────────────────────────────────────────────────────────
function loadDotEnv() {
  for (const p of [join(process.cwd(), '.env'), join(PROJECT_ROOT, '.env')]) {
    let raw;
    try { raw = readFileSync(p, 'utf8'); } catch { continue; }
    for (const line of raw.split('\n')) {
      const t = line.trim();
      if (!t || t.startsWith('#')) continue;
      const eq = t.indexOf('=');
      if (eq === -1) continue;
      const key = t.slice(0, eq).trim();
      let val = t.slice(eq + 1).trim();
      if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'")))
        val = val.slice(1, -1);
      if (key && !(key in process.env)) process.env[key] = val;
    }
    return p;
  }
}

function requireEnv(name) {
  const v = (process.env[name] || '').trim();
  if (!v) { console.error(`Missing env var: ${name}`); process.exit(1); }
  return v;
}

// ── Shopify REST helpers ───────────────────────────────────────────────────────
let SHOP, TOKEN, API_BASE;

async function shopifyFetch(path) {
  const res = await fetch(`${API_BASE}${path}`, {
    headers: { 'X-Shopify-Access-Token': TOKEN, 'Content-Type': 'application/json' },
  });
  return res.json();
}

// ── Collection descriptions ───────────────────────────────────────────────────
const COLLECTION_DESCRIPTIONS = {
  'women-dresses': {
    eyebrow: 'Effortless silhouettes',
    description: 'Flowy maxi dresses, embroidered anarkalis, and cotton everyday styles for festive and casual occasions.',
  },
  'women-kurta-sets': {
    eyebrow: 'Polished festive sets',
    description: 'Anarkali kurta sets, block print co-ords, and embroidered ensembles crafted for family celebrations.',
  },
  'women-unstitched-suits': {
    eyebrow: 'Curated fabric sets',
    description: 'Handpicked unstitched suit sets — jamdani, appliqué, block print, and hand-painted — ready to be stitched to fit.',
  },
  'kids-boys': {
    eyebrow: 'Festive boys wear',
    description: 'Kurta sets, Nehru jackets, and ethnic separates for little celebrations, pujas, and family occasions.',
  },
  'kids-girls': {
    eyebrow: 'Little celebration wear',
    description: 'Dresses, lehenga sets, and ethnic pieces with color, movement, and a traditional finish.',
  },
};

// ── Fetch collections from Shopify ───────────────────────────────────────────
async function fetchCollections() {
  const { custom_collections } = await shopifyFetch(
    '/custom_collections.json?limit=250&fields=id,handle,title,products_count'
  );
  const { smart_collections } = await shopifyFetch(
    '/smart_collections.json?limit=250&fields=id,handle,title,products_count'
  );
  return [...(custom_collections || []), ...(smart_collections || [])];
}

async function fetchProductCountForCollection(collectionId) {
  const { count } = await shopifyFetch(`/products/count.json?collection_id=${collectionId}`);
  return count || 0;
}

// ── Generate collectionConfig.js ─────────────────────────────────────────────
function generateConfig(collections) {
  const lines = [
    '// AUTO-GENERATED by scripts/sync-ui-collections.js — do not edit by hand',
    `// Last synced: ${new Date().toISOString()}`,
    '',
    'export const CURATED_COLLECTIONS = [',
  ];

  for (const col of collections) {
    const meta = COLLECTION_DESCRIPTIONS[col.handle] || {
      eyebrow: col.title,
      description: `${col.title} from Shriyam.`,
    };
    lines.push('  {');
    lines.push(`    handle: '${col.handle}',`);
    lines.push(`    title: '${col.title}',`);
    lines.push(`    eyebrow: '${meta.eyebrow}',`);
    lines.push(`    description: '${meta.description}',`);
    lines.push(`    productCount: ${col.productCount || 0},`);
    lines.push('    match: {');
    lines.push(`      tags: ['collection:${col.handle}'],`);
    lines.push('    },');
    lines.push('  },');
  }

  lines.push('];', '');

  // Re-export helpers unchanged
  lines.push(`export function getCuratedCollection(handle) {
  return CURATED_COLLECTIONS.find((c) => c.handle === handle);
}

export function productMatchesCollection(product, collection) {
  if (!product || !collection?.match) return false;
  const { productTypes = [], tags: matchingTags = [] } = collection.match;
  const tags = product.tags || [];
  return (
    productTypes.includes(product.productType) ||
    matchingTags.some((t) => tags.includes(t))
  );
}
`);

  return lines.join('\n');
}

// ── Navigation menu update ────────────────────────────────────────────────────
async function updateShopifyNav(collections) {
  // Build the nav items from Shriyam collections
  const collectionItems = collections.map(c => ({
    title: c.title,
    url: `/collections/${c.handle}`,
  }));

  // Try to update the "main-menu" link list via REST
  const { links_lists } = await shopifyFetch('/link_lists.json?fields=handle,id');
  const mainMenu = (links_lists || []).find(l => l.handle === 'main-menu');
  if (!mainMenu) {
    console.log('  No main-menu link list found — skipping nav update');
    return;
  }

  const baseLinks = [{ title: 'Home', url: '/' }];
  const shopLink = { title: 'Shop', url: '/collections/all' };
  const collLink = collectionItems;
  const links = [...baseLinks, shopLink, ...collLink];

  const res = await fetch(`${API_BASE}/link_lists/${mainMenu.id}.json`, {
    method: 'PUT',
    headers: { 'X-Shopify-Access-Token': TOKEN, 'Content-Type': 'application/json' },
    body: JSON.stringify({
      link_list: {
        handle: 'main-menu',
        links: links.map(l => ({ title: l.title, url: l.url, type: 'http' })),
      },
    }),
  });
  const json = await res.json();
  if (json.errors) {
    console.log('  Nav update skipped (link_list may not support REST writes on Hydrogen)');
  } else {
    console.log(`  Nav updated with ${links.length} items`);
  }
}

// ── Main ──────────────────────────────────────────────────────────────────────
async function main() {
  loadDotEnv();
  SHOP = requireEnv('SHOPIFY_SHOP_DOMAIN');
  TOKEN = requireEnv('SHOPIFY_ADMIN_API_ACCESS_TOKEN');
  const version = process.env.SHOPIFY_API_VERSION || '2026-04';
  API_BASE = `https://${SHOP}/admin/api/${version}`;

  console.log(`Store: ${SHOP}`);
  console.log('Fetching collections...');

  const allCollections = await fetchCollections();
  console.log(`Found ${allCollections.length} collections`);

  // Filter to known Shriyam collections (prioritize ours, then others)
  const shriyamHandles = new Set(Object.keys(COLLECTION_DESCRIPTIONS));
  const shriyamCollections = allCollections.filter(c => shriyamHandles.has(c.handle));

  // Enrich with product counts
  for (const col of shriyamCollections) {
    col.productCount = await fetchProductCountForCollection(col.id);
    console.log(`  ${col.title}: ${col.productCount} products`);
  }

  // Sort by a logical display order
  const displayOrder = [
    'women-dresses', 'women-kurta-sets', 'women-unstitched-suits',
    'kids-girls', 'kids-boys',
  ];
  shriyamCollections.sort((a, b) => {
    const ai = displayOrder.indexOf(a.handle);
    const bi = displayOrder.indexOf(b.handle);
    return (ai === -1 ? 99 : ai) - (bi === -1 ? 99 : bi);
  });

  const newConfig = generateConfig(shriyamCollections);

  if (DRY_RUN) {
    console.log('\n[DRY RUN] Would write to app/lib/collectionConfig.js:\n');
    console.log(newConfig);
    return;
  }

  writeFileSync(CONFIG_FILE, newConfig, 'utf8');
  console.log(`\n✓ Wrote ${CONFIG_FILE}`);

  // Update Shopify navigation menu
  await updateShopifyNav(shriyamCollections);

  console.log('\n✓ UI sync complete. Restart the dev server to pick up changes.');
}

main().catch(err => { console.error(err); process.exit(1); });
